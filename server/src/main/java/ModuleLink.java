public class ModuleLink {
    String id;
    String title;
    String description;
    String format;
    String artifactFormat;
    String type;
    String linkTitle;

    public ModuleLink(String id, String title, String description, String format, String artifactFormat, String type, String linkTitle){
        this.id = id;
        this.title = title;
        this.description = description;
        this.format = format;
        this.artifactFormat = artifactFormat;
        this.type = type;
        this.linkTitle = linkTitle;

    }
}
